package api;

public class Sample {

    private int id;
    private String name;

    public Sample() {
        this.id = 10;
        this.name = "Raja";
    }

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

}